                <br>
                <p>Help! est le cinquième album des Beatles, paru le 6 août 1965 au Royaume-Uni, et une semaine plus tard aux États-Unis. 
                Il est préparé dans le cadre d'un deuxième film les mettant en vedette, également intitulé Help!. 
                Les Fab Four l'enregistrent au cours de plusieurs sessions réparties entre les concerts et les tournages, entre février et juin 1965. 
                Ils se tournent de plus en plus vers le travail en studio, y répétant directement les nouveaux titres en les enregistrant, multipliant les overdubs, approchant de nouveaux sons et de nouveaux instruments sur le chemin qui mène, plus tard dans l'année, à Rubber Soul, étape majeure de leur carrière discographique.</p>
                <br>
                <p>Avec cet album, les Beatles entrent de plain-pied dans la musique pop. 
                La face A de Help! comprend les chansons composées pour le film, notamment la chanson éponyme et Ticket to Ride, parues en single. 
                L'autre face contient pour sa part sept chansons absentes du long-métrage, dont les deux dernières reprises officiellement mises en boîte par le groupe, et Yesterday, enregistrée par Paul McCartney, seul avec sa guitare et un quatuor à cordes, produisant un des plus grands succès de l'histoire de la musique.</p>
                <br>
                <p>L'album connaît un important succès critique et commercial, trônant neuf semaines en tête des hit-parades britanniques, après y être entré directement à la première place le jour de sa publication, avant de se maintenir 41 semaines dans les classements. 
                La version publiée aux États-Unis qui atteindra la position de tête durant 9 semaines, mais désapprouvée par les Beatles, est nettement différente ; elle ne comprend en effet que les chansons de la face A, intercalées entre des morceaux instrumentaux issus du film. 
                Les autres chansons de l'album britannique se retrouvent sur les albums américains Beatles VI, Rubber Soul et Yesterday and Today. 
                Les titres de Help! sont donc répartis sur quatre disques, qui sont autant de no 1 sur le marché américain.</p>
                
